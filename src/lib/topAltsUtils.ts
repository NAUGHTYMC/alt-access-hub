export const calculateSuccessRate = (delivered: number, total: number): number => {
  if (total === 0) return 0;
  return Math.round((delivered / total) * 100);
};

export const formatAccountString = (email: string, password: string): string => {
  return `${email}:${password}`;
};

export const parseAccountString = (accountString: string): { email: string; password: string } | null => {
  const parts = accountString.split(':');
  if (parts.length >= 2) {
    return {
      email: parts[0],
      password: parts.slice(1).join(':') // Handle passwords with colons
    };
  }
  return null;
};